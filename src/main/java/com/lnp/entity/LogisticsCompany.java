package com.lnp.entity;

public class LogisticsCompany {

    private int companyId;//物流公司编号

    private String companyName;//物流公司名称

    private String companyCoding;//物力公司查询编码

    public int getCompanyId() {
        return companyId;
    }

    public void setCompanyId(int companyId) {
        this.companyId = companyId;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyCoding() {
        return companyCoding;
    }

    public void setCompanyCoding(String companyCoding) {
        this.companyCoding = companyCoding;
    }



}
